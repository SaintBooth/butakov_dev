type GradientKey = 'bitrix' | 'ai' | 'b2b' | 'wordpress';

const caseGradients: Record<GradientKey, string> = {
  bitrix: 'from-blue-500 to-cyan-400',
  ai: 'from-teal-500 to-emerald-400',
  b2b: 'from-indigo-500 to-blue-500',
  wordpress: 'from-amber-500 to-orange-500',
};

interface CaseInput {
  title: string;
  category: string;
  description: string;
  fullDescription?: string;
  problem?: string;
  solution?: string;
  metric: string;
  result?: string;
  tags: string[];
  techStack?: string[];
  gradientKey: GradientKey;
}

function createCase(id: number, data: CaseInput): PortfolioCase {
  return {
    id,
    title: data.title,
    category: data.category,
    description: data.description,
    fullDescription: data.fullDescription,
    problem: data.problem,
    solution: data.solution,
    metric: data.metric,
    result: data.result ?? data.metric,
    tags: data.tags,
    techStack: data.techStack ?? data.tags,
    gradient: caseGradients[data.gradientKey],
  };
}

